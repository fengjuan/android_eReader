
package com.example.minireader.entity;

import java.io.Serializable;

/**
 *@author fengjuan, 2014-4-9
 *
 */

public class BookNote implements Serializable{
	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;
	private int noteId;
	private String bookName;
	private String noteTitle;
	private String noteContent;
	
	public BookNote() {}

	public BookNote(String bookName, String noteTitle, String noteContent) {
		this.bookName = bookName;
		this.noteTitle = noteTitle;
		this.noteContent = noteContent;
	}

	public int getNoteId() {
		return noteId;
	}

	public void setNoteId(int noteId) {
		this.noteId = noteId;
	}

	public String getNoteTitle() {
		return noteTitle;
	}

	public void setNoteTitle(String noteTitle) {
		this.noteTitle = noteTitle;
	}

	public String getNoteContent() {
		return noteContent;
	}

	public void setNoteContent(String noteContent) {
		this.noteContent = noteContent;
	}

	public String getBookName() {
		return bookName;
	}

	public void setBookName(String bookName) {
		this.bookName = bookName;
	}

	@Override
	public String toString() {
		return "BookNote [noteId=" + noteId + ", bookName=" + bookName + ", noteTitle=" + noteTitle
				+ ", noteContent=" + noteContent + "]";
	}
	
}
